class Tutor {
  final String id;
  final String name;
  final String subject;
  final double rating;
  final double price;
  final List<String> skills;
  final String avatar;

  const Tutor({
    required this.id,
    required this.name,
    required this.subject,
    required this.rating,
    required this.price,
    required this.skills,
    required this.avatar,
  });
}
